public class CalReport {
    private Long yi;
    private Long er;
    private Long san;
    private Long si;
    private Long wu;
    private Long liu;
    private Long qi;
    private Long ba;
    private Long jiu;
    private Long shi;
    private Long shiyi;
    private Long shier;

    public CalReport(Long yi, Long er, Long san, Long si, Long wu, Long liu, Long qi, Long ba, Long jiu, Long shi, Long shiyi, Long shier) {
        this.yi = yi;
        this.er = er;
        this.san = san;
        this.si = si;
        this.wu = wu;
        this.liu = liu;
        this.qi = qi;
        this.ba = ba;
        this.jiu = jiu;
        this.shi = shi;
        this.shiyi = shiyi;
        this.shier = shier;
    }

    public CalReport() {
    }

    public Long getYi() {
        return yi;
    }

    public void setYi(Long yi) {
        this.yi = yi;
    }

    public Long getEr() {
        return er;
    }

    public void setEr(Long er) {
        this.er = er;
    }

    public Long getSan() {
        return san;
    }

    public void setSan(Long san) {
        this.san = san;
    }

    public Long getSi() {
        return si;
    }

    public void setSi(Long si) {
        this.si = si;
    }

    public Long getWu() {
        return wu;
    }

    public void setWu(Long wu) {
        this.wu = wu;
    }

    public Long getLiu() {
        return liu;
    }

    public void setLiu(Long liu) {
        this.liu = liu;
    }

    public Long getQi() {
        return qi;
    }

    public void setQi(Long qi) {
        this.qi = qi;
    }

    public Long getBa() {
        return ba;
    }

    public void setBa(Long ba) {
        this.ba = ba;
    }

    public Long getJiu() {
        return jiu;
    }

    public void setJiu(Long jiu) {
        this.jiu = jiu;
    }

    public Long getShi() {
        return shi;
    }

    public void setShi(Long shi) {
        this.shi = shi;
    }

    public Long getShiyi() {
        return shiyi;
    }

    public void setShiyi(Long shiyi) {
        this.shiyi = shiyi;
    }

    public Long getShier() {
        return shier;
    }

    public void setShier(Long shier) {
        this.shier = shier;
    }
}
