package contract.pojo;

public class Chart6 {
    String htfl;
    String ssbm;
    String sum;

    public Chart6() {
    }

    public Chart6(String htfl, String ssbm, String sum) {
        this.htfl = htfl;
        this.ssbm = ssbm;
        this.sum = sum;
    }

    public String getHtfl() {
        return htfl;
    }

    public void setHtfl(String htfl) {
        this.htfl = htfl;
    }

    public String getSsbm() {
        return ssbm;
    }

    public void setSsbm(String ssbm) {
        this.ssbm = ssbm;
    }

    public String getSum() {
        return sum;
    }

    public void setSum(String sum) {
        this.sum = sum;
    }
}
